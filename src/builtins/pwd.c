#include "../../includes/minishell.h"

void	pwd(void)
{
	char	*pwd;

	pwd = getcwd(NULL, 0);
	if (!pwd)
	{
		ft_exit(1, 0, 0);
		return ;
	}
	ft_printf("%s\n", pwd);
	ft_exit(0, 0, 0);
	free(pwd);
}

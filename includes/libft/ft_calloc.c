#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*alloc;

	if ((int)(nmemb * size) < 0 || ((int)size < 0 && (int)nmemb < 0))
		return (NULL);
	alloc = NULL;
	alloc = malloc(nmemb * size);
	if (!alloc)
		return (NULL);
	ft_bzero(alloc, nmemb * size);
	return (alloc);
}
